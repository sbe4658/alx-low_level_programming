/**
 * add - addition function.
 * @a: 1st int.
 * @b: 2nd int.
 *
 * Return: the result.
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - substraction function.
 * @a: 1st int.
 * @b: 2nd int.
 *
 * Return: the result.
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplication function.
 * @a: 1st int.
 * @b: 2nd int.
 *
 * Return: the result.
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - division function.
 * @a: 1st int.
 * @b: 2nd int.
 *
 * Return: the result.
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - Modulo function.
 * @a: 1st int.
 * @b: 2nd int.
 *
 * Return: the result.
 */
int mod(int a, int b)
{
	return (a % b);
}
