/**
 * add - returns the results.
 * @a: first number.
 * @b: second number.
 *
 * Return: the result.
 */
int add(int a, int b)
{
	return a + b;
}
/**
 * sub - returns the results.
 * @a: first number.
 * @b: second number.
 *
 * Return: the result.
 */
int sub(int a, int b)
{
	return a - b;
}
/**
 * mul - returns the results.
 * @a: first number.
 * @b: second number.
 *
 * Return: the result.
 */
int mul(int a, int b)
{
	return a * b;
}
/**
 * div - returns the results.
 * @a: first number.
 * @b: second number.
 *
 * Return: the result.
 */
int div(int a, int b)
{
	return a / b;
}
/**
 * mod - returns the results.
 * @a: first number.
 * @b: second number.
 *
 * Return: the result.
 */
int mod(int a, int b)
{
	return a % b;
}
