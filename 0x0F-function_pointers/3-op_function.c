#include "3-calc.h"
/**
 * op_add - addition function.
 * @a: 1st int.
 * @b: 2nd int.
 *
 * Return: the result.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substraction function.
 * @a: 1st int.
 * @b: 2nd int.
 *
 * Return: the result.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication function.
 * @a: 1st int.
 * @b: 2nd int.
 *
 * Return: the result.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division function.
 * @a: 1st int.
 * @b: 2nd int.
 *
 * Return: the result.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Modulo function.
 * @a: 1st int.
 * @b: 2nd int.
 *
 * Return: the result.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
